    /**
     * Trả những cuốn sách đã chọn vào cơ sở dữ liệu
     *
     * @param evt
     */
    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        if (!ReturnBookFrame.dataTable.isEmpty()) {
            try {
                boolean[] id = new boolean[ReturnBookFrame.dataTable.size()];
                for (int i = 0; i < ReturnBookFrame.dataTable.size(); i++) {
                    id[i] = Boolean.valueOf(model.getValueAt(i, 6).toString());
                    ReturnBookFrame.dataTable.get(i)[6] = String.valueOf(model.getValueAt(i, 4).toString());
                    ReturnBookFrame.dataTable.get(i)[7] = String.valueOf(model.getValueAt(i, 5).toString());
                }
                new ReturnBook_Controller().ReturnBook(ReturnBookFrame.dataTable, id);
                new ReturnBook_Controller().UpdateInfoBorrowTable(ReturnBookFrame.IdInfoBorrow);

                ReturnBookFrame.siP.getSearchBorrowerP().SearchBookIsBorrowed();
                updateTable();
            } catch (SQLException ex) {
                Logger.getLogger(tableBookInfoBorrow.class.getName()).log(Level.SEVERE, null, ex);
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(tableBookInfoBorrow.class.getName()).log(Level.SEVERE, null, ex);
            } catch (ParseException ex) {
                Logger.getLogger(tableBookInfoBorrow.class.getName()).log(Level.SEVERE, null, ex);
            }
        }

    }//GEN-LAST:event_jButton1MouseClicked


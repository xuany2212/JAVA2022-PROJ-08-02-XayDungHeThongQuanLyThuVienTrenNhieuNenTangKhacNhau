    /**
     * Cập nhật lại bảng những cuốn sách còn mượn sau khi trả sách
     */
    public void updateTable() {
        if (!ReturnBookFrame.dataTable.isEmpty()) {
            model.setRowCount(0);
        }
        for (int i = 0; i < ReturnBookFrame.dataTable.size(); i++) {
            Object[] newRow = {
                ReturnBookFrame.dataTable.get(i)[2],
                ReturnBookFrame.dataTable.get(i)[3],
                ReturnBookFrame.dataTable.get(i)[4],
                ReturnBookFrame.dataTable.get(i)[5],
                ReturnBookFrame.dataTable.get(i)[6],
                ReturnBookFrame.dataTable.get(i)[7],
                ReturnBookFrame.dataTable.get(i)[8],};
            model.addRow(newRow);
        }
    }


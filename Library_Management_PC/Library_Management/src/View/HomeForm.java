    public void loadBook() throws ClassNotFoundException, SQLException{
        books = Book_Controller.getInstance().getListBook();
       genTableBook(books);
    }
    
    private void genTableBook(ArrayList<Book> books){
        DefaultTableModel model = (DefaultTableModel) tableBook.getModel();
        model.setRowCount(0);
        for (Book b : books){
            model.addRow(new Object[]{
                b.getbook_ID(),
                b.getbook_Title(),
                b.getbook_Publisher().getpublisher_Name(),
                b.getbook_Author()
            });    
        }
        
    }
    

    private void searchBook() throws ClassNotFoundException, SQLException {
        if (tfSearchBook.getText().toString().isEmpty()){
                loadBook();
        } else {
            String key = tfSearchBook.getText().toString();
            ArrayList<Book> arr = Book_Controller.getInstance().searchBook(key);
            genTableBook(arr);
        }
    }


